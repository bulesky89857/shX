main_menu() {
    while true; do
        show_banner
        
        echo -e "${BOLD}${WHITE}请选择要执行的功能:${RESET}\n"
        echo -e "  ${GREEN}1)${RESET} 创建SSH密钥对"
        echo -e "  ${GREEN}2)${RESET} 配置SSH安全设置"
        echo -e "  ${GREEN}3)${RESET} 查看SSH配置详情"
        echo -e "  ${GREEN}4)${RESET} 修改SSH端口\n"  # 新增功能
        
