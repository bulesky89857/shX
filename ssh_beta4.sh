        echo -e "\n  ${BOLD}${RED}0)${RESET} ${YELLOW}退出脚本${RESET}\n"
        
        echo -n "请输入选项 (0-4): "
        read -r MENU_CHOICE
        
        case $MENU_CHOICE in
            1)
                function_create_keys
                ;;
            2)
                function_ssh_config
                ;;
            3)
                function_show_config
                ;;
            4)
                function_change_port
                ;;
            0)
                echo -e "\n${ICON_OK} ${GREEN}感谢使用，再见！${RESET}\n"
                exit 0
